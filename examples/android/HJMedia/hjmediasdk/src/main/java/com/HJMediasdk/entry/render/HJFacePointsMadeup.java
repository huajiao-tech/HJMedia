    public List<FacePoint> getFacePoints(int imgIdx, int[] outImgIdx) {
        if (frames.isEmpty()) {
            return Collections.emptyList();
        }
        int useIdx = imgIdx;
        if (imgIdx == -1) {
            useIdx = index;
        } else if (imgIdx < 0 || imgIdx >= frames.size()) {
            useIdx = Math.floorMod(imgIdx, frames.size());
        }
        if (outImgIdx != null && outImgIdx.length > 0) {
            outImgIdx[0] = useIdx;
        }
        if (imgIdx == -1) {
            index = (index + 1) % frames.size();
        }
        return frames.get(useIdx);
    }

    private List<List<FacePoint>> buildFrames() {
        List<List<FacePoint>> result = new ArrayList<>(POINTS.length);
        for (float[][] frame : POINTS) {
            List<FacePoint> pts = new ArrayList<>(frame.length);
            for (float[] xy : frame) {
                pts.add(new FacePoint(xy[0], xy[1]));
            }
            result.add(Collections.unmodifiableList(pts));
        }
        return Collections.unmodifiableList(result);
    }

